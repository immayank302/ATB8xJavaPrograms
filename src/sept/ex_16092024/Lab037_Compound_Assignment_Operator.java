package sept.ex_16092024;

public class Lab037_Compound_Assignment_Operator {

    public static void main(String[] args) {

        // Compound Assignment Operators
        // +=, -=, *=, /=

        int a=10;
        a+=20; // a=a+20
        System.out.println(a);
        a-=5; // a=a-5
        System.out.println(a);
        a*=4; // a=a*4
        System.out.println(a);
        a/=5;
        System.out.println(a);

        a%=3;
        System.out.println(a);



    }
}
