package sept.ex_16092024;

public class Lab031_Arithmetic_Operator {

    public static void main(String[] args) {

        // Arithmetic Operator
        // '+', '-', '*', '/', '%'

        double a=24;
        double b=13;
      // Sum of the 2 numbers
        System.out.println(a+b);
       // Subtract of the 2 numbers
        System.out.println(a-b);
        // Multiply 2 numbers
        System.out.println(a*b);
        // Divide 2 numbers
        System.out.println(a/b);
        // Modulus of 2 numbers
        System.out.println(a%b);



    }
}
