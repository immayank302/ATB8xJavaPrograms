package sept.ex_25092024;

public class Lab099 {

    public static void main(String[] args) {

        // To use the char data type

        for (char c='a';c<='z';c++){
            System.out.println(c);
        }
        // It will be seen that when we apply the for loop using the char data type it will not print the ASCII numbers here.
        // Instead it will print the characters from a to z.
    }
}
