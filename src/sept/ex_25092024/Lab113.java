package sept.ex_25092024;

public class Lab113 {
    public static void main(String[] args) {

        // Using decrement
        int i1=10;  // If we have i1=0 then the below while statement will be false . It will not print anything
        while (i1>=1){
              System.out.println(i1);
              i1++;
 // If we have the int i1=10 and then the condition i1>=1 and i1++ then it will print the infinite loop.
        }
    }
}
